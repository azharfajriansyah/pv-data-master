    @Override
    protected void populateData(String searchQuery) {
        listData = new ArrayList<Pelanggan>();
        listData = repository.populate(searchQuery);
        
        removeTableData();
        
        DefaultTableModel model = (DefaultTableModel) tblPelanggan.getModel();
        for(Pelanggan obj : listData) {
            model.addRow(new Object[] {obj.nama, obj.jenisKelamin, obj.noTelepon, obj.alamat});
        }
    }

    @Override
    protected void save(Pelanggan object) {
        try {
            repository.save(object);
            populateData(txtCari.getText());
            clearForm();
        } catch (Exception e) {
            AFDialog.showError(this, e.getLocalizedMessage());
        }
    }

    @Override
    protected void update(Pelanggan object) {
        try {
            repository.update(object);
            populateData(txtCari.getText());
            clearForm();
            dataToEdit = null;
        } catch (Exception e) {
            AFDialog.showError(this, e.getLocalizedMessage());
        }
    }

    @Override
    protected void delete(String id) {
        try {
            repository.delete(id);
            populateData(txtCari.getText());
            clearForm();
        } catch (Exception e) {
            AFDialog.showError(this, e.getLocalizedMessage());
        }
    }

    @Override
    protected void clearForm() {
        txtIdPelanggan.setText("");
        txtNamaPelanggan.setText("");
        txtNoTelepon.setText("");
        txtAlamat.setText("");
        rbLaki.setSelected(false);
        rbPerempuan.setSelected(false);
        btnGroupJenisKelamin.clearSelection();
    }

    @Override
    protected void fillForm(Pelanggan object) {
        txtIdPelanggan.setText(object.id);
        txtNamaPelanggan.setText(object.nama);
        txtNoTelepon.setText(object.noTelepon);
        txtAlamat.setText(object.alamat);
        rbLaki.setSelected(("Laki-Laki".equals(object.jenisKelamin)));
        rbPerempuan.setSelected("Perempuan".equals(object.jenisKelamin));
    }
    
    @Override
    protected void removeTableData() {
        ((DefaultTableModel) tblPelanggan.getModel()).setRowCount(0);
    }
