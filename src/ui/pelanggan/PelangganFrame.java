    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String id = txtIdPelanggan.getText();
        String nama = txtNamaPelanggan.getText();
        String jenis = (rbLaki.isSelected() ? "Laki-Laki" : (rbPerempuan.isSelected() ? "Perempuan" : ""));
        String noTelepon = txtNoTelepon.getText();
        String alamat = txtAlamat.getText();

        if (id.isEmpty()) {
            AFDialog.showError(this, "ID Pelanggan harus diisi.");
            return;
        } else if (nama.isEmpty()) {
            AFDialog.showError(this, "Nama Pelanggan harus diisi.");
            return;
        } else if (jenis.isEmpty()) {
            AFDialog.showError(this, "Jenis Kelamin harus diisi.");
            return;
        } else if (noTelepon.isEmpty()) {
            AFDialog.showError(this, "No Telepon harus diisi.");
            return;
        } else if (alamat.isEmpty()) {
            AFDialog.showError(this, "Alamat harus diisi.");
            return;
        }

        if (dataToEdit != null) {
            update(new Pelanggan(dataToEdit.id, nama, jenis, noTelepon, alamat));
        } else {
            save(new Pelanggan(id, nama, jenis, noTelepon, alamat));
        }

    }//GEN-LAST:event_btnSimpanActionPerformed

