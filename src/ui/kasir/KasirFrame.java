    @Override
    protected void populateData(String searchQuery) {
        listData = new ArrayList<Kasir>();
        listData = repository.populate(searchQuery);
        
        removeTableData();
        
        DefaultTableModel model = (DefaultTableModel) tblKasir.getModel();
        for(Kasir obj : listData) {
            model.addRow(new Object[] {obj.nama, obj.jenisKelamin, obj.noTelepon, obj.agama, obj.alamat});
        }
    }

    @Override
    protected void save(Kasir object) {
        try {
            repository.save(object);
            populateData(txtCari.getText());
            clearForm();
        } catch (Exception e) {
            AFDialog.showError(this, e.getLocalizedMessage());
        }
    }

    @Override
    protected void update(Kasir object) {
        try {
            repository.update(object);
            populateData(txtCari.getText());
            clearForm();
            dataToEdit = null;
        } catch (Exception e) {
            AFDialog.showError(this, e.getLocalizedMessage());
        }
    }

    @Override
    protected void delete(String id) {
        try {
            repository.delete(id);
            populateData(txtCari.getText());
            clearForm();
        } catch (Exception e) {
            AFDialog.showError(this, e.getLocalizedMessage());
        }
    }

    @Override
    protected void clearForm() {
        txtIdKasir.setText("");
        txtNamaKasir.setText("");
        txtNoTelepon.setText("");
        cbAgama.setSelectedIndex(0);
        txtAlamat.setText("");
        rbLaki.setSelected(false);
        rbPerempuan.setSelected(false);
        btnGroupJenisKelamin.clearSelection();
        txtPassword.setText("");
    }

    @Override
    protected void fillForm(Kasir object) {
        txtIdKasir.setText(object.id);
        txtNamaKasir.setText(object.nama);
        txtNoTelepon.setText(object.noTelepon);
        cbAgama.setSelectedItem(object.agama);
        txtAlamat.setText(object.alamat);
        rbLaki.setSelected(("Laki-Laki".equals(object.jenisKelamin)));
        rbPerempuan.setSelected("Perempuan".equals(object.jenisKelamin));
        txtPassword.setText(object.password);
    }
    
    @Override
    protected void removeTableData() {
        ((DefaultTableModel) tblKasir.getModel()).setRowCount(0);
    }
