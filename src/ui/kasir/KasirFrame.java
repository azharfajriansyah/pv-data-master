    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String id = txtIdKasir.getText();
        String nama = txtNamaKasir.getText();
        String jenis = (rbLaki.isSelected() ? "Laki-Laki" : (rbPerempuan.isSelected() ? "Perempuan" : ""));
        String noTelepon = txtNoTelepon.getText();
        String agama = cbAgama.getSelectedItem().toString();
        String alamat = txtAlamat.getText();
        String password = txtPassword.getText();

        if (id.isEmpty()) {
            AFDialog.showError(this, "ID Kasir harus diisi.");
            return;
        } else if (nama.isEmpty()) {
            AFDialog.showError(this, "Nama Kasir harus diisi.");
            return;
        } else if (jenis.isEmpty()) {
            AFDialog.showError(this, "Jenis Kelamin harus diisi.");
            return;
        } else if (noTelepon.isEmpty()) {
            AFDialog.showError(this, "No Telepon harus diisi.");
            return;
        } else if (agama.isEmpty()) {
            AFDialog.showError(this, "Agama harus diisi.");
            return;
        } else if (alamat.isEmpty()) {
            AFDialog.showError(this, "Alamat harus diisi.");
            return;
        } else if (password.isEmpty()) {
            AFDialog.showError(this, "Password harus diisi.");
            return;
        }

        if (dataToEdit != null) {
            update(new Kasir(dataToEdit.id, nama, jenis, noTelepon, agama, alamat, password));
        } else {
            save(new Kasir(id, nama, jenis, noTelepon, agama, alamat, password));
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

