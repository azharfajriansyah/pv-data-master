    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String kode = txtKodeBarang.getText();
        String nama = txtNamaBarang.getText();
        String jenis = txtJenisBarang.getText();
        String hargaBeli = txtHargaBeli.getText();
        String hargaJual = txtHargaJual.getText();

        if (kode.isEmpty()) {
            AFDialog.showError(this, "Kode barang harus diisi.");
            return;
        } else if (nama.isEmpty()) {
            AFDialog.showError(this, "Nama barang harus diisi.");
            return;
        } else if (jenis.isEmpty()) {
            AFDialog.showError(this, "Jenis barang harus diisi.");
            return;
        } else if (hargaBeli.isEmpty()) {
            AFDialog.showError(this, "Harga beli harus diisi.");
            return;
        } else if (hargaJual.isEmpty()) {
            AFDialog.showError(this, "Harga jual harus diisi.");
            return;
        }

        if (dataToEdit != null) {
            update(new Barang(dataToEdit.id, kode, nama, jenis, Integer.parseInt(hargaBeli), Integer.parseInt(hargaJual)));
        } else {
            save(new Barang(0, kode, nama, jenis, Integer.parseInt(hargaBeli), Integer.parseInt(hargaJual)));
        }

    }//GEN-LAST:event_btnSimpanActionPerformed

