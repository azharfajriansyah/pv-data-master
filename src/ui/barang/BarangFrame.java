    @Override
    protected void populateData(String searchQuery) {
        listData = new ArrayList<Barang>();
        listData = repository.populate(searchQuery);
        
        removeTableData();
        
        DefaultTableModel model = (DefaultTableModel) tblBarang.getModel();
        for(Barang obj : listData) {
            model.addRow(new Object[] {obj.kode, obj.nama, obj.jenis, obj.hargaBeli, obj.hargaJual});
        }
    }
    
    @Override
    protected void save(Barang object) {
        try {
            repository.save(object);
            populateData(txtCari.getText());
            clearForm();
        } catch (Exception e) {
            AFDialog.showError(this, e.getLocalizedMessage());
        }
    }

    @Override
    protected void update(Barang object) {
        try {
            repository.update(object);
            populateData(txtCari.getText());
            clearForm();
            dataToEdit = null;
        } catch (Exception e) {
            AFDialog.showError(this, e.getLocalizedMessage());
        }
    }

    @Override
    protected void delete(String id) {
        try {
            repository.delete(String.valueOf(id));
            populateData(txtCari.getText());
            clearForm();
        } catch (Exception e) {
            AFDialog.showError(this, e.getLocalizedMessage());
        }
    }
    
    @Override
    protected void removeTableData() {
        ((DefaultTableModel) tblBarang.getModel()).setRowCount(0);
    }
    
    @Override
    protected void clearForm() {
        txtKodeBarang.setText("");
        txtNamaBarang.setText("");
        txtJenisBarang.setText("");
        txtHargaBeli.setText("");
        txtHargaJual.setText("");
    }
    
    @Override
    protected void fillForm(Barang barang) {
        txtKodeBarang.setText(barang.kode);
        txtNamaBarang.setText(barang.nama);
        txtJenisBarang.setText(barang.jenis);
        txtHargaBeli.setText(""+barang.hargaBeli+"");
        txtHargaJual.setText(""+barang.hargaJual+"");
    }
